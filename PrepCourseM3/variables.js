var bootcamp = "Henry";       // Ejemplo de cadena de texto


// Number (Número): Representa valores numéricos, ya sean enteros o decimales.
var numeroEntero = 17;        // Ejemplo de número entero
var numeroNegativo = -4;      // Ejemplo de número negativo
var numeroDecimal = 11.75;    // Ejemplo de número decimal


// Boolean (Booleano): Representa un valor verdadero (true) o falso (false).
var estoyEntendiendo = true;  // Ejemplo de valor booleano verdadero
var estoyAburrido = false;    // Ejemplo de valor booleano falso


